#include <QApplication>
#include <QDesktopWidget>

#include "utils.h"

void recenterWidget(QWidget *w)
{
	QRect screen = QApplication::desktop()->screenGeometry();
	w->move((screen.width() - w->width()) / 2, (screen.height() - w->height()) / 2);
}
